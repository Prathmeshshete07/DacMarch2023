public class Ass1Q7 {
    public static void main(String[] args) {
        // byte 
        System.out.println("The number of bits used to represent a byte value : "+Byte.SIZE);
        System.out.println(" The number of bytes used to represent a byte value : "+Byte.BYTES);
        System.out.println("The minimum value a byte : "+Byte.MIN_VALUE);
        System.out.println("The maximum value a byte : "+Byte.MAX_VALUE);

        // int
        System.out.println("The number of bits used to represent a int value : "+Integer.SIZE);
        System.out.println(" The number of bytes used to represent a int value : "+Integer.BYTES);
        System.out.println("The minimum value a int : "+Integer.MIN_VALUE);
        System.out.println("The maximum value a int : "+Integer.MAX_VALUE);

        // double
        System.out.println("The number of bits used to represent a double value : "+Double.SIZE);
        System.out.println(" The number of bytes used to represent a double value : "+Double.BYTES);
        System.out.println("The minimum value a double : "+Double.MIN_VALUE);
        System.out.println("The maximum value a double : "+Double.MAX_VALUE);

    }
    
}
